package com.backend.dto;

public class Approvals {

	String clearance_name;
	
	String caf_id;
	
	String proposal_no;

	

	public String getClearance_name() {
		return clearance_name;
	}

	public void setClearance_name(String clearance_name) {
		this.clearance_name = clearance_name;
	}

	public String getCaf_id() {
		return caf_id;
	}

	public void setCaf_id(String caf_id) {
		this.caf_id = caf_id;
	}

	public String getProposal_no() {
		return proposal_no;
	}

	public void setProposal_no(String proposal_no) {
		this.proposal_no = proposal_no;
	}
	
	

	
	
	
	
}
